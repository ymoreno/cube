package com.xpertgroup.cube.input;

public class InputQuery {

    Integer x1Coordinate;
    Integer y1Coordinate;
    Integer z1Coordinate;
    Integer x2Coordinate;
    Integer y2Coordinate;
    Integer z2Coordinate;

    public InputQuery(Integer x1Coordinate, Integer y1Coordinate, Integer z1Coordinate, Integer x2Coordinate, Integer y2Coordinate, Integer z2Coordinate) {
        this.x1Coordinate = x1Coordinate;
        this.y1Coordinate = y1Coordinate;
        this.z1Coordinate = z1Coordinate;
        this.x2Coordinate = x2Coordinate;
        this.y2Coordinate = y2Coordinate;
        this.z2Coordinate = z2Coordinate;
    }

    public Integer getX1Coordinate() {
        return x1Coordinate;
    }

    public void setX1Coordinate(Integer x1Coordinate) {
        this.x1Coordinate = x1Coordinate;
    }

    public Integer getY1Coordinate() {
        return y1Coordinate;
    }

    public void setY1Coordinate(Integer y1Coordinate) {
        this.y1Coordinate = y1Coordinate;
    }

    public Integer getZ1Coordinate() {
        return z1Coordinate;
    }

    public void setZ1Coordinate(Integer z1Coordinate) {
        this.z1Coordinate = z1Coordinate;
    }

    public Integer getX2Coordinate() {
        return x2Coordinate;
    }

    public void setX2Coordinate(Integer x2Coordinate) {
        this.x2Coordinate = x2Coordinate;
    }

    public Integer getY2Coordinate() {
        return y2Coordinate;
    }

    public void setY2Coordinate(Integer y2Coordinate) {
        this.y2Coordinate = y2Coordinate;
    }

    public Integer getZ2Coordinate() {
        return z2Coordinate;
    }

    public void setZ2Coordinate(Integer z2Coordinate) {
        this.z2Coordinate = z2Coordinate;
    }
}
